// Build caption groups from per-scene whisper transcripts.
// Reads assets/narration/<scene>.transcript.json (flat [{text,start,end}]),
// groups words into short readable phrases, applies a small correction map for
// known whisper mishears (proper nouns it can't spell), and writes
// assets/captions.json keyed by scene with scene-local timing.
//
// Re-run after re-generating narration:  node scripts/build-captions.mjs
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const N = join(root, "assets", "narration");

// Whisper transcribes the synthesized speech accurately but can't spell brand
// names. Phrase-level replacements applied to the final grouped string (catches
// multi-token mishears, plus brand-name spelling).
const PHRASE_FIX = [
  [/\bWhile drive\b/gi, "WalDrive"],
  [/\bWhiledrive\b/gi, "WalDrive"],
  [/\bWaldrive\b/g, "WalDrive"],
  [/\bagent store\b/gi, "agents store"],
  [/\bOneClick\b/g, "One click"],
  [/\brefetches\b/gi, "re-fetches"],
  [/\bbackhand\b/gi, "backend"],
  [/\bSUI\b/g, "Sui"],
];

const MAX_WORDS = 6; // measured/calm narration → longer groups
const MIN_WORDS = 2;
const PAUSE = 0.34; // gap (s) that forces a new group

function groupWords(words) {
  const groups = [];
  let cur = [];
  const flush = () => {
    if (!cur.length) return;
    groups.push({
      text: cur.map((w) => w.text).join(" "),
      start: cur[0].start,
      end: cur[cur.length - 1].end,
    });
    cur = [];
  };
  for (let i = 0; i < words.length; i++) {
    const w = words[i];
    const prev = words[i - 1];
    const gap = prev ? w.start - prev.end : 0;
    const endsSentence = prev && /[.!?]$/.test(prev.text);
    if (cur.length >= MIN_WORDS && (endsSentence || gap > PAUSE) ) flush();
    cur.push(w);
    if (cur.length >= MAX_WORDS) flush();
  }
  flush();
  return groups;
}

function fixGroupText(s) {
  let out = s;
  for (const [re, rep] of PHRASE_FIX) out = out.replace(re, rep);
  // Capitalize first letter of each group for readability.
  out = out.replace(/^([a-z])/, (_, c) => c.toUpperCase());
  return out;
}

const out = {};
for (const file of readdirSync(N).sort()) {
  const m = file.match(/^(\d\d-[a-z]+)\.transcript\.json$/);
  if (!m) continue;
  const scene = m[1];
  const words = JSON.parse(readFileSync(join(N, file), "utf8"));
  const groups = groupWords(words).map((g) => ({
    text: fixGroupText(g.text),
    start: +g.start.toFixed(2),
    end: +g.end.toFixed(2),
  }));
  out[scene] = groups;
}

writeFileSync(join(root, "assets", "captions.json"), JSON.stringify(out, null, 2));
// captions.js is loaded synchronously by index.html before the timeline script,
// so caption data is available at timeline-build time without an async fetch.
writeFileSync(
  join(root, "captions.js"),
  "// AUTO-GENERATED by scripts/build-captions.mjs — do not edit by hand.\n" +
    "window.__captions = " + JSON.stringify(out, null, 2) + ";\n",
);
const total = Object.values(out).reduce((n, g) => n + g.length, 0);
console.log(`Wrote assets/captions.json + captions.js — ${Object.keys(out).length} scenes, ${total} caption groups.`);
for (const [scene, gs] of Object.entries(out)) {
  console.log(`  ${scene}: ${gs.length} groups, last end ${gs[gs.length - 1].end}s`);
}
